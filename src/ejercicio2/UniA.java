public static void mostrarFichero() {
  
        FileReader fr = null;
        try {
            File fichero = new File("universidadA.txt");
            fr = new FileReader(fichero);
            BufferedReader br = new BufferedReader(fr);
            leerFichero(br);
        } catch (Exception e) {
           JOptionPane.showMessageDialog(null, e.getMessage());
        } finally {
            try {
                if (fr != null) {
                    fr.close();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
 
    }
 public static void leerFichero(BufferedReader br) throws Exception {
      
        String linea, linea2="";
        linea = br.readLine();
        while (linea != null) {
            linea2 = linea2+"\n"+linea;
            linea=br.readLine();
            
 
        }
        
        JOptionPane.showMessageDialog(null,linea2);
    }
    
