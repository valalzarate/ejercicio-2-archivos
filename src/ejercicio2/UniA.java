public static void mouseHerramientaMisteriosa(int op, String busqueda1, String busqueda2){
          FileReader frA = null;
          FileWriter fw1 = null, fw2 = null;
          
          try {
            //abro el archivo de MayoresA para escritura
            fw1 = new FileWriter(busqueda1 + ".txt");
            BufferedWriter bw1 = new BufferedWriter(fw1);
            PrintWriter pw1 = new PrintWriter(bw1);
            
            //abro el archivo de menoresA para escritura
            fw2 = new FileWriter(busqueda2 + ".txt");
            BufferedWriter bw2 = new BufferedWriter(fw2);
            PrintWriter pw2 = new PrintWriter(bw2);
            
            //abro el archivo de universidadA para lectura
            File ficheroA = new File("universidadA.txt");
            frA = new FileReader(ficheroA);         
            BufferedReader brA = new BufferedReader(frA); 
            
            String line, line1 = "", line2 = "";
            
            while (brA.ready()) {
            line = brA.readLine();
            String [] registro = line.split(", ");
  
              if(op ==0){
                 if (Integer.parseInt(registro[4])>= 18){                  
                   pw1.print(line);
                   pw1.println();
                   line1 = line1 +"\n"+line;
                 }
                 if(Integer.parseInt(registro[4]) < 18){ 
                   pw2.print(line);
                   pw2.println(); 
                   line2 = line2 +"\n"+line;
                 } 
             }
             if (op == 1){ 
                 if(registro[3].equals("femenino")){
                     
                     pw1.print(line);
                     pw1.println();
                     line1 = line1 +"\n"+line;
                 }
                 if(registro[3].equals("masculino")){
                     pw2.print(line);
                     pw2.println();
                     line2 = line2 +"\n"+line;
                 }
                 
             }
            }
             
                    pw1.close();
                    bw1.close();
                    fw1.close();
                    pw2.close();
                    bw2.close();
                    fw2.close();
                    
                    if (op == 0){
                        JOptionPane.showMessageDialog(null,"MAYORES DE EDAD"+ "\n" +line1); 
                        JOptionPane.showMessageDialog(null,"MENORES DE EDAD"+"\n"+line2);
                    }else{
                        JOptionPane.showMessageDialog(null,"SEXO FEMENINO"+ "\n" +line1); 
                        JOptionPane.showMessageDialog(null,"SEXO MASCULINO"+"\n"+line2);
                    }
                        
        } catch (Exception e) {
           JOptionPane.showMessageDialog(null, e.getMessage());
        } finally {
            try {
                if (frA != null) {
                    frA.close();
                }
              
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
       
    }    

